/**
 * SOP 定义 - 各学术类型的固定步骤及引导文本
 */

export interface SopStep {
  step: number;
  name: string;
  prompt: string;
}

export interface SopDefinition {
  category: string;
  name: string;
  welcome: string;
  steps: SopStep[];
  completion: string;
}

